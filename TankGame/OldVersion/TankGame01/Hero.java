package TankGame01;

/**
 * The type Tank 01.
 */
public class Hero extends Tank{

    /**
     * Instantiates a new Tank 01.
     *
     * @param x the x
     * @param y the y
     */
    public Hero(int x, int y) {
        super(x, y);
    }

    @Override
    public int getX() {
        return super.getX();
    }

    @Override
    public void setX(int x) {
        super.setX(x);
    }

    @Override
    public int getY() {
        return super.getY();
    }

    @Override
    public void setY(int y) {
        super.setY(y);
    }
}
