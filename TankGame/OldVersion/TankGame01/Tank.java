package TankGame01;

/**
 * The type Tank.
 */
public class Tank {

    private int x; // x-axis
    private int y;  // y-axis

    /**
     * Instantiates a new Tank.
     *
     * @param x the x
     * @param y the y
     */
    public Tank(int x, int y) {
        this.x = x;
        this.y = y;
    }

    /**
     * Gets x.
     *
     * @return the x
     */
    public int getX() {
        return x;
    }

    /**
     * Sets x.
     *
     * @param x the x
     */
    public void setX(int x) {
        this.x = x;
    }

    /**
     * Gets y.
     *
     * @return the y
     */
    public int getY() {
        return y;
    }

    /**
     * Sets y.
     *
     * @param y the y
     */
    public void setY(int y) {
        this.y = y;
    }
}
